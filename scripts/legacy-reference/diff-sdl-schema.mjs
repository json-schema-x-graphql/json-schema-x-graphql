#!/usr/bin/env node
import fs from 'fs';
import path from 'path';
import { parse } from 'graphql';

const repoRoot = path.resolve(new URL(import.meta.url).pathname, '..', '..');
const sdlPath = path.join(repoRoot, 'src', 'data', 'schema_unification.graphql');
const schemaPath = path.join(repoRoot, 'src', 'data', 'schema_unification.schema.json');
const outPath = path.join(repoRoot, 'generated-metadata', 'schema-diff.md');

if (!fs.existsSync(sdlPath) || !fs.existsSync(schemaPath)) {
  console.error('Missing source files');
  process.exit(1);
}

const sdl = fs.readFileSync(sdlPath,'utf8');
const schema = JSON.parse(fs.readFileSync(schemaPath,'utf8'));

function collectGraphQLTypeFieldsMap(sdl) {
  const ast = parse(sdl);
  const map = new Map();
  for (const def of ast.definitions) {
    if (def.kind === 'ObjectTypeDefinition') {
      const name = def.name.value;
      const fields = (def.fields || []).map(f => f.name.value);
      map.set(name, fields);
    }
  }
  return map;
}

function collectJsonSchemaPropertySet(schema) {
  const out = new Set();
  function walk(node, prefix='') {
    if (!node || typeof node !== 'object') return;
    if (node.properties && typeof node.properties === 'object') {
      for (const k of Object.keys(node.properties)) {
        out.add(prefix + k);
        walk(node.properties[k], prefix + k + '.');
      }
    }
    if (node.items) walk(node.items, prefix);
    if (node.anyOf) node.anyOf.forEach(n => walk(n, prefix));
    if (node.oneOf) node.oneOf.forEach(n => walk(n, prefix));
    if (node.allOf) node.allOf.forEach(n => walk(n, prefix));
  }
  walk(schema);
  return out;
}

const gqlMap = collectGraphQLTypeFieldsMap(sdl);
const jsonKeys = collectJsonSchemaPropertySet(schema);

// For each GraphQL type, list fields missing in JSON schema (by simple name match)
const rows = [];
for (const [type, fields] of gqlMap.entries()) {
  const missing = fields.filter(f => !jsonKeys.has(f));
  if (missing.length) {
    missing.forEach(f => rows.push({ type, field: f }));
  }
}

// Also capture top-level JSON schema properties not in any GraphQL field list
const gqlFieldSet = new Set();
for (const fields of gqlMap.values()) fields.forEach(f => gqlFieldSet.add(f));
const extraJson = Array.from(jsonKeys).filter(k => !gqlFieldSet.has(k));

let md = '# SDL vs JSON Schema Diff Report\n\n';
md += 'Generated by `scripts/diff-sdl-schema.mjs`\n\n';
md += '## Missing fields in JSON Schema (grouped by GraphQL type)\n\n';
if (rows.length === 0) md += '- None\n';
else {
  md += '| GraphQL Type | Missing Field |\n|---:|---|\n';
  rows.slice(0,1000).forEach(r => { md += `| ${r.type} | ${r.field} |\n`; });
}

md += '\n## JSON Schema properties with no matching GraphQL field\n\n';
if (extraJson.length === 0) md += '- None\n';
else {
  md += extraJson.slice(0,1000).map(x => `- ${x}`).join('\n') + '\n';
}

fs.writeFileSync(outPath, md, 'utf8');
console.log('Wrote', outPath);
process.exit(0);
